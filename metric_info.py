def get_comparison(metric_name: str, value: float):
    """
    指標の値を理想値(目安)と比べて、表示用のテキストを返す。
    理想値が無い指標、または該当する指標が無い場合は None を返す。
    """
    info = METRIC_INFO.get(metric_name)
    if not info or info.get("ideal_range") is None:
        return None

    low, high = info["ideal_range"]
    if low <= value <= high:
        return "✅ 目安の範囲内です"
    elif value < low:
        return f"🔵 目安(およそ{low}〜{high})よりやや小さめです"
    else:
        return f"🔵 目安(およそ{low}〜{high})よりやや大きめです"


def get_description(metric_name: str) -> str:
    info = METRIC_INFO.get(metric_name)
    return info["description"] if info else ""


def get_ideal_note(metric_name: str) -> str:
    info = METRIC_INFO.get(metric_name)
    return info["ideal_note"] if info else ""
